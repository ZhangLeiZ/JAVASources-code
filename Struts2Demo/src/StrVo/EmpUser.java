package StrVo;

import java.util.Date;

public class EmpUser {

	private String Empid;
	private String Empname;
	private int Empage;
	private String Empsex;
	private String Empcardno;
	private Date Empdate;
	private int Empemile;
	private String Empthoer;
	public String getEmpid() {
		return Empid;
	}
	public void setEmpid(String empid) {
		Empid = empid;
	}
	public String getEmpname() {
		return Empname;
	}
	public void setEmpname(String empname) {
		Empname = empname;
	}
	public int getEmpage() {
		return Empage;
	}
	public void setEmpage(int empage) {
		Empage = empage;
	}
	public String getEmpsex() {
		return Empsex;
	}
	public void setEmpsex(String empsex) {
		Empsex = empsex;
	}
	public String getEmpcardno() {
		return Empcardno;
	}
	public void setEmpcardno(String empcardno) {
		Empcardno = empcardno;
	}
	public int getEmpemile() {
		return Empemile;
	}
	public void setEmpemile(int empemile) {
		Empemile = empemile;
	}
	public String getEmpthoer() {
		return Empthoer;
	}
	public void setEmpthoer(String empthoer) {
		Empthoer = empthoer;
	}
	public Date getEmpdate() {
		return Empdate;
	}
	public void setEmpdate(Date empdate) {
		Empdate = empdate;
	}
}
